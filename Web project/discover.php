    <h1>For you who want to get more knowledge on Healthcare:</h1>
    <h3>As Government set regulations  also ever-changing and can affect how healthcare
    professionals provide care to their patients, so this is another important
    area in which to stay informed. Fortunately, self-education doesn’t have to
    be a strenuous task. Many healthcare workers subscribe to medical journals
    or newsletters in order to receive the latest research and some continue to
    educate themselves in a very informal way through conversations with
    colleagues. Grabbing a cup of coffee with another healthcare professional,
    especially someone who works at a different hospital or has a different
    specialization, can provide educational insights as well as an opportunity
    to socialize.</h3>
