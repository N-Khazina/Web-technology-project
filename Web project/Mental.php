      Caring for Your Mental Health Esta página también está disponible en
      español. Overview Mental health includes emotional, psychological, and
      social well-being. It affects how we think, feel, act, make choices, and
      relate to others. Mental health is more than the absence of a mental
      illness—it’s essential to your overall health and quality of life.
      Self-care can play a role in maintaining your mental health and help
      support your treatment and recovery if you have a mental illness. About
      Self-Care Self-care means taking the time to do things that help you live
      well and improve both your physical health and mental health. When it
      comes to your mental health, self-care can help you manage stress, lower
      your risk of illness, and increase your energy. Even small acts of
      self-care in your daily life can have a big impact. Here are some tips to
      help you get started with self-care: Get regular exercise. Just 30 minutes
      of walking every day can help boost your mood and improve your health.
      Small amounts of exercise add up, so don’t be discouraged if you can’t do
      30 minutes at one time. Eat healthy, regular meals and stay hydrated. A
      balanced diet and plenty of water can improve your energy and focus
      throughout the day. Also, limit caffeinated beverages such as soft drinks
      or coffee. Make sleep a priority. Stick to a schedule, and make sure
      you’re getting enough sleep. Blue light from devices and screens can make
      it harder to fall asleep, so reduce blue light exposure from your phone or
      computer before bedtime. Try a relaxing activity. Explore relaxation or
      wellness programs or apps, which may incorporate meditation, muscle
      relaxation, or breathing exercises. Schedule regular times for these and
      other healthy activities you enjoy such as journaling. Set goals and
      priorities. Decide what must get done now and what can wait. Learn to say
      “no” to new tasks if you start to feel like you’re taking on too much. Try
      to be mindful of what you have accomplished at the end of the day, not
      what you have been unable to do. Practice gratitude. Remind yourself daily
      of things you are grateful for. Be specific. Write them down at night, or
      replay them in your mind. Focus on positivity. Identify and challenge your
      negative and unhelpful thoughts. Stay connected. Reach out to your friends
      or family members who can provide emotional support and practical help.
      Self-care looks different for everyone, and it is important to find what
      you need and enjoy. It may take trial and error to discover what works
      best for you. In addition, although self-care is not a cure for mental
      illnesses, understanding what causes or triggers your mild symptoms and
      what coping techniques work for you can help manage your mental health.
      For other ideas for healthy practices for your mind, body, surroundings,
      and relationships, see the National Institutes of Health (NIH) Wellness
      Toolkits. When to Seek Professional Help Seek professional help if you are
      experiencing severe or distressing symptoms that have lasted two weeks or
      more, such as: Difficulty sleeping Appetite changes that result in
      unwanted weight changes Struggling to get out of bed in the morning
      because of mood Difficulty concentrating Loss of interest in things you
      usually find enjoyable Inability to perform usual daily functions and
      responsibilities Don’t wait until your symptoms are overwhelming. Talk
      about your concerns with your primary care provider, who can refer you to
      a mental health specialist if needed. If you don’t know where to start,
      read the National Institute of Mental Health (NIMH) Tips for Talking With
      Your Health Care Provider fact sheet. Learn more about how to get help or
      find a provider on the NIMH’s Help for Mental Illnesses webpage. What to
      Do in a Crisis If you are thinking about harming yourself or attempting
      suicide, tell someone who can help right away or dial 911 in an emergency.
      You also can call the National Suicide Prevention Lifeline (Lifeline) at
      1‑800‑273‑TALK (8255), or text the Crisis Text Line (text HELLO to
      741741). Both services are free and available 24 hours a day, 7 days a
      week. All calls are confidential. Contact social media outlets directly if
      you are concerned about a friend’s social media updates, or dial 911 in an
      emergency. Learn more on the Lifeline website or the Crisis Text Line
      website. For additional information about suicide prevention, please see
      NIMH’s Suicide Prevention webpage.
